async function main() {
  let added = 0;
  for (const { category, names } of SETS) {
    for (const name of names) {
      if (name.includes('精')) continue; // guard against any stray bad char
      const exists = await prisma.listing.findFirst({ where: { name, type: 'DRINK' } });
      if (exists) continue;
      await prisma.listing.create({
        data: { name, type: 'DRINK', category, source: 'catalog' },
      });
      added++;
    }
  }
  console.log(`Сорта/виды напитков добавлены: ${added}`);
  await prisma.$disconnect();
}
main().catch(async (e) => { console.error(e); await prisma.$disconnect(); process.exit(1); });
