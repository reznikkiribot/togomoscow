/** Line id + colour for a station name (falls back to a neutral grey). */
export function metroLineOf(station) {
  const id = STATION_LINE[station];
  const line = id ? METRO_LINES[id] : null;
  return line ? { line: id, lineName: line.name, color: line.color } : { line: null, lineName: null, color: '#7A7A7A' };
}
