const norm = (s) =>
  s.toLowerCase().replace(/[’'`"«».,()\-_/]+/g, '').replace(/\s+/g, ' ').trim();

async function main() {
  const rows = [];
  for (const [name, category] of DISHES) {
    rows.push({
      type: 'DISH', name, category, source: 'catalog',
      externalId: 'dish/' + norm(name), groupKey: norm(name),
      avgRating: 0, reviewCount: 0,
    });
  }
  for (const [name, category] of DRINKS) {
    rows.push({
      type: 'DRINK', name, category, source: 'catalog',
      externalId: 'drink/' + norm(name), groupKey: norm(name),
      avgRating: 0, reviewCount: 0,
    });
  }
  const r = await prisma.listing.createMany({ data: rows, skipDuplicates: true });
  const dishes = await prisma.listing.count({ where: { type: 'DISH' } });
  const drinks = await prisma.listing.count({ where: { type: 'DRINK' } });
  console.log(`Каталог: добавлено ${r.count}. Всего блюд: ${dishes}, напитков: ${drinks}.`);
  await prisma.$disconnect();
}

main().catch(async (e) => {
  console.error(e);
  await prisma.$disconnect();
  process.exit(1);
});
